    /**
 * Affiche le classement pour le niveau sélectionné
 */
    private void afficherClassement() {
        if (cmbNiveauClassement == null || cmbNiveauClassement.getItemCount() == 0) {
            DefaultTableModel emptyModel = new DefaultTableModel(
                new String[]{"N° Étudiant", "Nom", "Moyenne"}, 0);
            tblClassement.setModel(emptyModel);
            return;
        }

        Object selectedObj = cmbNiveauClassement.getSelectedItem();
        if (selectedObj == null) return;

        String niveau = selectedObj.toString();
        
        try {
            // Appeler API pour recuperer le classement pre-calcule
            var classement = ApiClient.getClassement(niveau);
            
            if (classement == null || classement.size() == 0) {
                DefaultTableModel emptyModel = new DefaultTableModel(
                new String[]{"N° Étudiant", "Nom", "Moyenne"}, 0);
                tblClassement.setModel(emptyModel);
                JOptionPane.showMessageDialog(this, 
                    "Aucun étudiant trouvé pour le niveau " + niveau,
                    "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            // Créer le tableau
            DefaultTableModel model = new DefaultTableModel(
                new String[]{"N° Étudiant", "Nom", "Moyenne"}, 0
            );
            
            for (var element : classement) {
                var obj = element.getAsJsonObject();
                String ne = obj.get("ne").getAsString();
                String nom = obj.get("nom").getAsString();
                double moyenne = obj.get("moyenne").getAsDouble();
                model.addRow(new Object[]{ne, nom, String.format("%.2f", moyenne)});
            }
            
            tblClassement.setModel(model);
    //      centrerColonnesTable(tblClassement);
            tblClassement.setRowHeight(30);
        } catch (Exception e) {
            System.err.println("Erreur classement: " + e.getMessage());
            
            if (e.getMessage().contains("404")) {
                DefaultTableModel emptyModel = new DefaultTableModel(
                new String[]{"N° Étudiant", "Nom", "Moyenne"}, 0);
                tblClassement.setModel(emptyModel);
                JOptionPane.showMessageDialog(this, 
                    "Aucun étudiant trouvé pour le niveau " + niveau,
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }
    
    
