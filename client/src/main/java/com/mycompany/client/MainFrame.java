        private void afficherNotesFiltrees() {
//        if (toutesLesNotes == null) {
//            toutesLesNotes = ApiClient.getNotes();
//        }
            
        List<Note> notesAfficher = toutesLesNotes;
        
        // Filtrer les etudiants si selectionnee
        if (cmbFiltreNotesEtudiant != null && cmbFiltreNotesEtudiant.getSelectedIndex() > 0) {
            Object selected = cmbFiltreNotesEtudiant.getSelectedItem();
            if (selected != null && !selected.equals("Tous les étudiants")) {
                String selectedStr = selected.toString();
                String ne = selectedStr.split(" - ")[0];
                notesAfficher = toutesLesNotes.stream()
                    .filter(n -> n.getNe().equals(ne))
                    .collect(Collectors.toList());
            }
        }

//        List<Etudiant> etudiants = ApiClient.getEtudiants();
//        List<Matiere> matieres = ApiClient.getMatieres();

        DefaultTableModel model = new DefaultTableModel(
            new String[]{"N° Étudiant", "Étudiant", "Matière", "Note"}, 0
        );

        for (Note note : notesAfficher) {
            String nomEtudiant = tousLesEtudiants.stream()
                .filter(e -> e.getNe().equals(note.getNe()))
                .findFirst()
                .map(Etudiant::getNom)
                .orElse("Inconnu");

            String libelleMatiere = toutesLesMatieres.stream()
                .filter(m -> m.getCodemat().equals(note.getCodemat()))
                .findFirst()
                .map(Matiere::getLibelle)
                .orElse("Inconnu");

            model.addRow(new Object[]{
                note.getNe(),
                nomEtudiant,
                libelleMatiere,
                note.getNote()
            });
        }

        tblNotes.setModel(model);
//        centrerColonnesTable(tblNotes);
        tblNotes.setRowHeight(30);
    }
 
    /**
    * Calcule et affiche le bulletin de l'étudiant sélectionné
    */
    private void afficherBulletin() {
        if (cmbEtudiant == null || cmbEtudiant.getItemCount() == 0) {
            afficherBulletinVide();
            return;
        }
        
        // Recuperer Etudiant selectionne
        Object selectedObj = cmbEtudiant.getSelectedItem();
        if (selectedObj == null) {
            afficherBulletinVide();
            return;
        }

        String selected = selectedObj.toString();
        
        String ne = selected.split(" - ")[0];    
        
        System.out.println("Demande bulletin pour: " + ne);

        try {
            // Appeler l'API pour recuperer le buletin pre-calcule
            JsonObject bulletin = ApiClient.getBulletin(ne);
            
            // Verifier que le bulletin n'est pas null
            if (bulletin == null) {
                afficherBulletinVide();
                JOptionPane.showMessageDialog(this, "Aucun bulletin pour cet étudiant",
                        "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            System.out.println("Bulletin reçu: " + bulletin.toString());
            
            // Veifier que les notes existe
            JsonElement notesElement = bulletin.get("notes");
            if (notesElement == null || notesElement.isJsonNull()) {
                System.err.println("Notes null pour: " + ne);
                afficherBulletinVide();
                JOptionPane.showMessageDialog(this, 
                    "Aucune note trouvée pour l'étudiant " + ne,
                    "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
//            var notes = bulletin.getAsJsonArray("notes");
            JsonArray notes = notesElement.getAsJsonArray();
            if (notes.size() == 0) {
                System.err.println("Notes vides pour: " + ne);
                afficherBulletinVide();
                JOptionPane.showMessageDialog(this, 
                    "Aucune note enregistrée pour l'étudiant " + ne,
                    "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        
            // CReer tableau des bulletins
            DefaultTableModel model = new DefaultTableModel(
               new String[]{"Matière", "Coefficient", "Note", "Note Pondérée"}, 0
            );

            double sommePonderee = 0;
            int sommeCoef = 0;

            for (JsonElement element : notes) {
                JsonObject note = element.getAsJsonObject();
                String libelle = note.get("libelle").getAsString();
                int coef = note.get("coef").getAsInt();

                double valeur = note.get("note").getAsDouble();
                double ponderee = note.get("note_ponderee").getAsDouble();
           
                sommePonderee += ponderee;
                sommeCoef += coef;
                
                model.addRow(new Object[]{
                    libelle,
                    coef,
                    valeur,
                    String.format("%.2f", ponderee)
                });
            }
        
            // Calculer la moyenne
            double moyenne = 0;
            JsonElement moyenneElement = bulletin.get("moyenne");
            
            if (moyenneElement != null && !moyenneElement.isJsonNull()) {
                moyenne = moyenneElement.getAsDouble();
            } else {
                // Calculer la moyenne localement si le backend ne la fournit pas
                moyenne = sommeCoef > 0 ? sommePonderee / sommeCoef : 0;
            }
            
            String observation = "ÉCHEC";
            JsonElement obsElement = bulletin.get("observation");
            if (obsElement != null && !obsElement.isJsonNull()) {
                observation = obsElement.getAsString();
            } else {
                observation = moyenne >= 10 ? "ADMIS" : (moyenne >= 7.5 ? "REDOUBLANT" : "ÉCHEC");
            }

            // Afficher la moyenne et l'obs
            tblBulletin.setModel(model);
            lblMoyenne.setText(String.format("%.2f", moyenne));
            lblObservation.setText(observation);

            // Appliquer une couleur a l'observation
            if (observation.equals("ADMIS")) {
                lblObservation.setForeground(new java.awt.Color(0, 150, 0)); // Vert
            } else if (observation.equals("REDOUBLANT")) {
                lblObservation.setForeground(new java.awt.Color(255, 165, 0)); // Orange
            } else {
                lblObservation.setForeground(java.awt.Color.RED); // Rouge
            }

    //        tblBulletin.setModel(model);
            tblBulletin.setRowHeight(30);
        } catch (Exception e) {
            System.err.println("Erreur bulletin: " + e.getMessage());            
            afficherBulletinVide();
            
            // Afficher Message plus claire
            if (e.getMessage().contains("404")) {
                JOptionPane.showMessageDialog(this, """
                                                    Bulletin non trouv\u00e9 pour cet \u00e9tudiant.
                                                    V\u00e9rifiez que l'\u00e9tudiant a des notes.""", 
                "Information", JOptionPane.INFORMATION_MESSAGE);
            }
//            JOptionPane.showMessageDialog(this,
//                    "Erreur chargement bulletin: " + e.getMessage(),
//                    "Erreur", JOptionPane.ERROR_MESSAGE);
//            afficherBulletinVide();
        }
    }
    
