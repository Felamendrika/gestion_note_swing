    /**
     * Charge les données en arrière-plan pour ne pas bloquer l'interface
     */
    private void chargerDonneesEnArrierePlan() {
        new Thread(() -> {
            try {
                // Charger les données depuis l'API
                tousLesEtudiants = ApiClient.getEtudiants();
                toutesLesMatieres = ApiClient.getMatieres();
                toutesLesNotes = ApiClient.getNotes();
                etudiantsFiltres = new ArrayList<>(tousLesEtudiants);
                
                // Mettre à jour l'interface dans le thread Swing
                SwingUtilities.invokeLater(() -> {
                    remplirToutesLesCombobox();
                    rafraichirTousLesAffichages();
                    isInitialized = true;
                });
            } catch (Exception e) {
                System.err.println("Erreur chargement données: " + e.getMessage());
                SwingUtilities.invokeLater(() -> {
                    JOptionPane.showMessageDialog(this, 
                        "Erreur de chargement des données: " + e.getMessage() + "\n" +
                        "Vérifiez que le backend est démarré (npm run dev dans le dossier server)",
                        "Erreur de connexion", 
                        JOptionPane.ERROR_MESSAGE);
                    // Afficher des données vides
                    afficherEtudiantsVides();
                    isInitialized = true;
                });
            }
        }).start();
    }
    
