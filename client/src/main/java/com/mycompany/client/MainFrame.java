    /**
    * Méthode principale d'affichage des étudiants (avec filtre et recherche)
    */ 
    private void afficherEtudiantsFiltres() {
        // Vérifier que tousLesEtudiants n'est pas null
//        if (tousLesEtudiants == null) {
//            try {
//                tousLesEtudiants = ApiClient.getEtudiants();
////            etudiantsFiltres = new ArrayList<>(tousLesEtudiants);
//            } catch (Exception ex) {
//                JOptionPane.showMessageDialog(this, "Erreur: " + ex.getMessage());
//            }
//        }
    
        String recherche = txtRecherche.getText().toLowerCase();
//        String niveauFiltre = (String) cmbFiltreNiveau.getSelectedItem();
           
        String niveauFiltre = "Tous";
        if (cmbFiltreNiveau != null && cmbFiltreNiveau.getSelectedItem() != null) {
            niveauFiltre = (String) cmbFiltreNiveau.getSelectedItem();
        }

        final String niveauFiltreFinal = niveauFiltre;
    
        // Filtrer les etudiants
        etudiantsFiltres = tousLesEtudiants.stream()
                .filter(e -> {
                    // Filtre par recherche (ne oou nom)
                    boolean matchRecherche = recherche.isEmpty() ||
                            e.getNe().toLowerCase().contains(recherche) ||
                            e.getNom().toLowerCase().contains(recherche);
                    
                    // Filtrer par niveau 
                    boolean matchNiveau = niveauFiltreFinal.equals("Tous") ||
                            e.getNiveau().equals(niveauFiltreFinal);
                    
                    return matchRecherche && matchNiveau;
                })
                .collect(Collectors.toList());
        
        // Afficher dans la table
        DefaultTableModel model = new DefaultTableModel(
                new String[]{"N° Étudiant", "Nom", "Niveau"}, 0
        );
        
        for (Etudiant e : etudiantsFiltres) {
            model.addRow(new Object[]{e.getNe(), e.getNom(), e.getNiveau()});
        }
        
        tblEtudiants.setModel(model);
//        centrerColonnesTable(tblEtudiants);
        tblEtudiants.setRowHeight(28);
    }
    
